AlsaPlayer need some good artwork. The following is needed:<br>
<ul>
	<li><p><b>AlsaPlayer Logo</b><br>
	The actual logo is fine but it show nothing else that an AlsaPlayer screenshot on a gimp generated background (or something similar). And worst of all, it doesn't scale very well.</p>
